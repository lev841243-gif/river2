/**
 * Маска российского номера для поля ввода.
 *
 * Раньше `+7 (___) ___-__-__` был только плейсхолдером: он пропадал на первом
 * символе, и человек набирал «+», «7», скобки и дефисы руками. Теперь эти
 * знаки подставляются сами, а от клиента нужны только цифры.
 */

/** Сколько цифр в российском номере вместе с кодом страны: 7 + 10. */
const RU_DIGITS = 11

/**
 * Привести ввод к виду `+7 (999) 123-45-67`.
 *
 * Возвращает строку как есть, если человек начал с кода ДРУГОЙ страны: на
 * русскую версию заходят и с зарубежным номером, и подставлять им +7 нельзя —
 * иначе номер не введёшь вообще. Поэтому же одинокий «+» остаётся «плюсом»:
 * это первый символ зарубежного номера, и стирать его нельзя.
 *
 * Пустой ввод возвращает пустую строку, а не «+7»: иначе поле нельзя было бы
 * стереть, а плейсхолдер никогда бы не показался.
 */
export function formatRuPhone(input: string): string {
  const trimmed = input.trim()
  if (trimmed.startsWith('+') && !trimmed.startsWith('+7')) return input

  let digits = input.replace(/\D/g, '')
  if (digits.length === 0) return ''

  // «8 999…» — привычная местная запись того же номера, что и «+7 999…».
  if (digits.startsWith('8')) digits = '7' + digits.slice(1)
  // Человек начал сразу с кода оператора («999…») — дописываем код страны.
  if (!digits.startsWith('7')) digits = '7' + digits
  digits = digits.slice(0, RU_DIGITS)

  const rest = digits.slice(1) // без кода страны
  let out = '+7'
  if (rest.length > 0) out += ` (${rest.slice(0, 3)}`
  // Скобка закрывается только с ЧЕТВЁРТОЙ цифры, а не с третьей. Иначе её
  // тут же дописывало обратно после Backspace, и поле «+7 (999)» нельзя
  // было стереть — курсор упирался в неё намертво.
  if (rest.length > 3) out += `) ${rest.slice(3, 6)}`
  if (rest.length > 6) out += `-${rest.slice(6, 8)}`
  if (rest.length > 8) out += `-${rest.slice(8, 10)}`
  return out
}

/** Что показать, когда человек только поставил курсор в пустое поле. */
export const RU_PHONE_START = '+7 ('

/**
 * Остался ли в поле один код страны — то есть по сути пусто.
 * Нужно на уходе из поля: «+7 (» надо убрать, иначе вместо «заполните телефон»
 * человек получит «некорректный номер», хотя он ничего не вводил.
 */
export function isRuPhoneEmpty(value: string): boolean {
  return value.replace(/\D/g, '').replace(/^7/, '').length === 0
}
